export interface Position {
    x: number;
    y: number;
}

/**
 * @interface Line
 * @prop {number} xa line starting x position
 * @prop {number} ya line starting y position
 * @prop {number} xb line ending x position
 * @prop {number} yb line ending y position
 */
export interface Line {
    xa: number;
    ya: number;
    xb: number;
    yb: number;
}
